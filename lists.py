#list declaration
friends = ["Kevin", "Karen", "Jim"]

#printing list item
print(friends[0]) #to print Kevin
print(friends[2]) #to print Jim

#indexing from the back
print(friends[-1]) #to print Jim

#indexing the last two elements
print(friends[1:])

friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"]
print(friends[1:3]) #to print Karen and Jim

#to modify elements
friends[1] = "Mike"
print(friends[1]) #to print Mike

