#print number
print(2)

#print decimal number
print(2.09)

#print negative number
print(-1.891)

#primitive operations
print(3 + 4.5)
print(3 * 4.5)
print(3 * 4 + 5)
print(3 * (4 + 5))

#modulo operator
print(10 % 3) #gives 1

#print number via variable
my_num = 5
print(my_num)

#convert num into string
my_num = 5
print(str(my_num))
print(str(my_num) + " my favorite number") 

#absolute value
my_num = -5
print(abs(my_num))

#power function
print(pow(3,2)) # 3^2 = 9

#round function
print(round(3.4)) #3
print(round(3.7)) #4

#importing libraries
from math import *

#leaving decimal remainder
print(floor(3.5))
